# Khai báo các biến để lưu trữ các phần tử trong biểu thức
base1 = 32
exponent1 = 0.2
base2 = 1/64
exponent2 = -0.25
base3 = 8/27
exponent3 = 1/3

# Thực hiện các phép tính và gán kết quả vào biến A
A = base1**exponent1 - base2**exponent2 + base3**exponent3

# In kết quả ra màn hình
print("Giá trị của biểu thức A là:", A)
