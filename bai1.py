a = float(input("Nhập số nguyên thứ nhất: "))
b = float(input("Nhập số nguyên thứ hai: "))
c = float(input("Nhập số nguyên thứ ba: "))
d = float(input("Nhập số nguyên thứ tư: "))
trung_binh_cong = (a + b + c + d) / 4
print("Trung bình cộng của 4 số là:", trung_binh_cong)
