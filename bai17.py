so1 = float(input("Nhập số nguyên thứ nhất: "))
so2 = float(input("Nhập số nguyên thứ hai: "))
so3 = float(input("Nhập số nguyên thứ ba: "))
max_so = max(so1, so2, so3)
min_so = min(so1, so2, so3)
print("Số lớn nhất là:", max_so)
print("Số nhỏ nhất là:", min_so)
